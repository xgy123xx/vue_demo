      <div class="content">
        <p>
          十多年前，马云死也不做游戏的话言犹在耳，十多年后，阿里游戏旗下《Area F2》，却因涉嫌抄袭《彩虹六号：围攻》而“名扬海外”，遭到育碧起诉。但是，被起诉的不是阿里游戏，而是苹果和谷歌。
          5月15日，育碧在美国加州洛杉矶联邦法院，向苹果和谷歌发起诉讼，要求后者在应用商店中下架手机游戏《Area F2》，诉讼的理由是《Area F2》侵犯了《彩虹六号：围攻》的知识产权。育碧表示，《Area F2》“几乎照抄”了《彩虹六号：围攻》，包括游戏UI、干员信息等内容存在大量雷同。
          截至目前，苹果和谷歌拒绝下架《Area F2》。
        </p>
        <p>《Area F2》由广州简悦信息科技有限公司（以下简称简悦科技）负责研发，最初游戏名称为《代号F2》。2019年10月，《Area F2》开启了首次测试。</p>
        <p>2020年4月15日，《Area F2》正式登录海外市场。目前，在美国苹果应用商店，这款游戏在免费游戏榜排名36，仍可正常访问。</p>
        <p>5月16日，在接受凤凰网科技采访时，简悦科技回应称，“尊重对方，没有侵权”。同时简悦科技表示，《Area F2》由简悦团队历时2年重金自主研发，目前正在通过法务与对方展开积极的沟通。</p>
        <p>实际上，在玩家群体中，对《Area F2》抄袭的指责由来已久。不管是游戏的主要玩法，还是干员技能设定，诸多方面都难免令人联想到《彩虹六号：围攻》。</p>
        <p>4月下旬，TapTap上已经有玩家反应，《Area F2》和《彩虹六号：围攻》过于“相似”，包括游戏内的地图、干员技能等，几乎照搬。</p>
        <p>
          另一方面，也有不少玩家对《Area F2》的玩法给予了肯定，表示这款游戏还不错，其中不乏有人表示“有彩六内味”。
          截
        </p>
        <p>止到5月19日，《Area F2》的TapTap评分已经降至5.6分。</p>
        <p>
          事情发展到这里，可能有人会提出疑问，为什么一些玩家明知道《Area F2》过于“借鉴”《彩虹六号：围攻》，但还是会对其给予好评？这恐怕要从《彩虹六号：围攻》一直无法在大陆过审说起。
          早在2018年5月18日，育碧在财报会议上正式宣布，由其研发的《彩虹六号：围攻》等游戏，将会登录Wegame平台，由腾讯负责中国地区的发行运营。作为育碧旗下代表性的电竞游戏，《彩虹六号：围攻》在中国市场颇受玩家青睐。
          不过，《彩虹六号：围攻》的过审之路，远比想象中要困难得多。同样是2018年，大火的“吃鸡”网游《绝地求生》，就因无法过审，迟迟无法进入中国市场。相比起《绝地求生》，《彩虹六号：围攻》在暴力程度上不遑多让。
          2018年11月3日，育碧宣布，为了适应亚洲地区的审查机制，对《彩虹六号：围攻》进行“视觉元素调整”，即对游戏中出现的暴力、血腥、赌博、性暗示等内容加以和谐。不少人认为，育碧此举是为了进入中国市场。
          然而，由于和谐版游戏为全球同步，并非区域特供，育碧的举动引发了全球玩家大范围的反对。一时间，Steam上出现了大量差评。几周之后，育碧迫于压力，宣布取消此次视觉元素调整计划，游戏将恢复原貌，事件才逐渐得以平息。
          不仅如此，在这起和谐事件中，一些中国玩家同样表示不需要和谐版。事实上，中国玩家是《彩虹六号：围攻》中第二多的玩家群体。对于部分国内玩家而言，《彩虹六号：围攻》这一竞技模式，的确得到了很多人的青睐。基于此，《Area F2》的玩法会被肯定，也就不足为奇了。
        </p>
        <p>近些年，国内手游市场发展迅猛，在“吃鸡”类游戏高地已经被腾讯、网易占领的情况下，简悦科技选择另辟蹊径，借鉴《彩虹六号：围攻》，发力海外市场，填补这一类型游戏在手游市场的空白，不失为一个聪明之举。</p>
        <p>虽然此次育碧发起了诉讼，但我们看到，育碧并没有起诉简悦科技，也没有起诉阿里游戏，而是起诉了谷歌、苹果。一方面，《Area F2》并未在中国大陆发行，育碧难以在中国对简悦科技发起诉讼。另一方面，安卓市场和苹果市场直接参与了《Area F2》销售，可以在美国作为被告加以诉讼。</p>
        <p>
          而《Area F2》的海外发行商Qookka Games，根据相关资料显示，该商标申请主体为简悦科技，商标国际分类为教育娱乐类。
          围绕游戏侵权展开的诉讼，原告方获胜的难度普遍偏大，而且案件审理过程大多都会旷日持久。
        </p>
        <p>2014年11月20日，乐动卓越向昆仑万维发起诉讼，称《超级MT》侵犯了《我叫MT》版权。2015年7月20日，乐动卓越终于艰难获胜，而裁决的关键并非抄袭，是不正当竞争。</p>
        <p>2017年，暴雪联合网易起诉4399游戏抄袭《守望先锋》，向上海市浦东新区人民法院提起诉讼。直到2019年，这起案件才最终尘埃落定，暴雪胜诉。</p>
        <p>
          2018年4月6日，蓝洞就抄袭《绝地求生》，向网易游戏《荒野行动》提起诉讼。不过令人意外的是，在日本火爆的《荒野行动》，得到了不少日本玩家的力挺。2019年3月，蓝洞撤销了对网易的诉讼，双方达成庭外和解。
          无独有偶，在网易《第五人格》上线之后，由于和《黎明杀机》高度相似，同样出现了涉嫌抄袭的风波。不过在舆论尚未发酵前，网易找到了《黎明杀机》的制作公司Behavior Interactive，双方展开了深度合作。
        </p>
